#pragma mark - Table view data source

- (NSInteger)numberOfSectionsInTableView:(UITableView *)tableView {
#warning Potentially incomplete method implementation.
    // Return the number of sections.
    return 1;
}

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section {
    // Return the number of rows in the section.
    return [[self.repository getAllCards] count];
}


- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath {
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:@"CFMCardTableViewCell" forIndexPath:indexPath];
    
    CFMCreditCard *card = [self.repository getAllCards][indexPath.row];
    
    cell.imageView.image = [card image];
    cell.textLabel.text = [card.creditCardNumber stringByReplacingCharactersInRange:NSMakeRange(0, 12) withString:@"**** "];
    
    // Configure the cell...
    
    return cell;
}


